package Array;

public class Array1 {
    public static void main(String[] args){
        int student1 = 90;
        int student2 = 80;
        int student3 = 70;
        int student4 = 60;
        int student5 = 50;
        // 반복문으로는 생성을 할 수없다. 수정은 가능하나 생성은 불가능하다.
        System.out.println("학생 1의 점수는" + student1);
        System.out.println("학생 2의 점수는" + student2);
        System.out.println("학생 3의 점수는" + student3);
        System.out.println("학생 4의 점수는" + student4);
        System.out.println("학생 5의 점수는" + student5);
        //선언 -> 초기화 의 단계에서 선언만 하면 자동으로 생성이 된다.
    }
}
